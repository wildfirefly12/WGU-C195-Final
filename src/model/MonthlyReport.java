package model;

public class MonthlyReport {
    private String month;
    private int count;

    public String getMonth() {
        return month;
    }

    public int getCount() {
        return count;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
